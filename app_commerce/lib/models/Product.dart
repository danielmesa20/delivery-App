class Product {
  //Attributes
  final String name;
  final String description;
  final double price;
  final bool available;
  final String imageURL;
  final String publicId;
  final String commerceId;

  //Constructor
  Product({
    this.name,
    this.description,
    this.price,
    this.available,
    this.imageURL,
    this.publicId,
    this.commerceId,
  });
}